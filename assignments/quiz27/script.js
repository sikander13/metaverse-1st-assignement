let alien_color = prompt("Chose Alien Color green, red or yellow");

switch (alien_color) {
    case "green":
        document.write("You have  earned 5 points");
        break;
    case "yellow":
        document.write("You have  earned 10 points");
        break;
    case "red":
        document.write("You have  earned 15 points" );
        break;        
}
